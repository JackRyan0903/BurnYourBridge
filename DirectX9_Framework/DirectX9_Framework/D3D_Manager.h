#include <Windows.h>


//================================================================================//
//  @클래스
//	@설명
//		Framework에서 쓰일 매니저 클래스 들의 조상 클래스
//		인스턴스 생성 불가(추상 클래스)
//  @포함 함수
//      1. initialize() : 매니저 클래스를 초기화 한다.
//      2. update() : 매니저 클래스를 업데이트 한다.
//      3. deinitialize() : 매니저 클래스를 종료 한다.
//================================================================================//
class CD3D_Manager
{
public:
	//================================================================================//
	//  @멤버 함수
	//  @설명 : 매니저 클래스를 초기화하는 함수(완전 가상 함수)
	//	@매개변수
	//		1. HINSTANCE : 메인 윈도우 프로세스의 인스턴스 객체.
	//		2. HWND : 메인 윈도우의 핸들 값.
	//================================================================================//
	virtual bool initialize(HINSTANCE &hInstance, HWND &hWnd) = 0;

	//================================================================================//
	//  @멤버 함수
	//  @설명 : 매 프레임 불리며 상태 업데이트를 할 함수(완전 가상 함수)
	//	@매개변수 : 없음
	//================================================================================//
	virtual bool update(void) = 0;

	//================================================================================//
	//  @멤버 함수
	//  @설명 : 매니저 클래스가 종료 될 때 불리며, 메모리 해제 등 기능(완전 가상 함수)
	//	@매개변수 : 없음
	//================================================================================//
	virtual void deinitialize(void) = 0;

public:
	CD3D_Manager(void){}
	virtual ~CD3D_Manager(void){}

private:
	//================================================================================//
	//  @복사 생성자
	//  @설명
	//		객체 복사 방지 private
	//		객체 복사 시 묵시적 형변환을 막기위해 explicit 키워드 사용
	//================================================================================//
	explicit CD3D_Manager(const CD3D_Manager& copy){}

	//================================================================================//
	//  @연산자 오버로딩=
	//  @설명
	//		객체 복사 방지 private
	//================================================================================//
	CD3D_Manager& operator=(const CD3D_Manager& copy){}
};

